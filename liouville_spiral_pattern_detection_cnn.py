def liouville_lambda(n):
    """Compute 位(n) = (-1)^惟(n), where 惟(n) is the number of prime factors counted with multiplicity"""
    if n < 1:
        return 0
    omega = 0
    i = 2
    while i * i <= n:
        while n % i == 0:
            omega += 1
            n //= i
        i += 1
    if n > 1:
        omega += 1
    return 1 if omega % 2 == 0 else -1

def generate_ulam_spiral(size):
    """Generate a 2D grid of 位(n) values in an Ulam spiral pattern"""
    grid = np.zeros((size, size), dtype=int)
    x, y = size // 2, size // 2
    dx, dy = 0, -1
    n = 1
    for _ in range(size * size):
        if 0 <= x < size and 0 <= y < size:
            grid[y, x] = liouville_lambda(n)
        if x == y or (x < y and x + y == size - 1) or (x > y and x + y == size):
            dx, dy = -dy, dx
        x, y = x + dx, y + dy
        n += 1
    return grid

